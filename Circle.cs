using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using SystemsEngineering;
using CAD;

namespace Mathematics
{
    public class Circle : Primitive
    {
        //  *****************************************************************************************
        //  DECLARATIONS
        //
        //  ************************************************************
        //  
        //  Identification
        private String _CircleID;
        //
        //  Data
        //
        //  Dimensions
        private Double _Radius;
        
        //  *****************************************************************************************


        //  ****************************************************************************************
        //  INITIALIZATIONS
        //
        //  ************************************************************

        //  *****************************************************************************************


        //  *****************************************************************************************
        //  ENUMERATIONS
        //
        //  ************************************************************

        //  *****************************************************************************************


        //  *****************************************************************************************
        //  CIRCLE CONSTRUCTOR
        //
        //  ************************************************************
        public Circle()
        {
            //
            //  Add the center point
            this.CenterPoint = new Point();
            //
            //  Set the 2-D primitive type to circle
            this.TwoDType = TwoDPrimitiveTypeEnum.Circle;
            //
            //  Set the Is 2-D flag
            this.Is2D = true;
        }
        //  *****************************************************************************************


        //  *****************************************************************************************
        //  PROPERTIES
        //
        //  ************************************************************
        //  
        //  Identification
        public String CircleID
        {
            set => _CircleID = value;
            get
            {
                return _CircleID;
            }
        }
        
        //
        //  Data
        //
        //  Dimensions
        public Double Radius
        {
            set => _Radius = value;
            get
            {
                return _Radius;
            }
        }
        
        //  *****************************************************************************************


        //  *****************************************************************************************
        //  METHODS
        //
        //  ************************************************************

        //  *****************************************************************************************


        //  *****************************************************************************************
        //  EVENTS
        //
        //  ************************************************************

        //  *****************************************************************************************
    }
}
