using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using SystemsEngineering;
using CAD;

namespace Mathematics
{
    public class Sphere : Primitive
    {
        //  *****************************************************************************************
        //  DECLARATIONS
        //
        //  ************************************************************
        //  
        //  Identification
        private String _SphereID;
        //
        //  Data
        //
        //  Dimensions
        private Point _CenterPoint;
        private Double _Radius;
        //  *****************************************************************************************


        //  ****************************************************************************************
        //  INITIALIZATIONS
        //
        //  ************************************************************

        //  *****************************************************************************************


        //  *****************************************************************************************
        //  ENUMERATIONS
        //
        //  ************************************************************

        //  *****************************************************************************************


        //  *****************************************************************************************
        //  SPHERE CONSTRUCTOR
        //
        //  ************************************************************
        public Sphere()
        {
            this.Is2D = false;
            this.ThreeDType = ThreeDPrimitiveTypeEnum.Sphere;

            //
            //  Location
            this.CenterPoint = new Point();
        }
        //  *****************************************************************************************


        //  *****************************************************************************************
        //  PROPERTIES
        //
        //  ************************************************************
        //  
        //  Identification
        public String SphereID
        {
            set => _SphereID = value;
            get
            {
                return _SphereID;
            }
        }
        //
        //  Data
        //
        //  Dimensions
        public Point CenterPoint
        {
            set => _CenterPoint = value;
            get
            {
                return _CenterPoint;
            }
        }
        public Double Radius
        {
            set => _Radius = value;
            get
            {
                return _Radius;
            }
        }
        //  *****************************************************************************************


        //  *****************************************************************************************
        //  METHODS
        //
        //  ************************************************************

        //  *****************************************************************************************


        //  *****************************************************************************************
        //  EVENTS
        //
        //  ************************************************************

        //  *****************************************************************************************
    }
}
